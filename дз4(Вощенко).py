text = "Колишня розпродажна виручка виявилася мрією. Все було продано, навіть ті товари, що лежали на полицях занадто довго. Щасливий вигляд покупців свідчив про успіх події. Але задоволення від такого результату затмарила пізніша звістка про пожежу в складському приміщенні."

# Перетворення тексту у верхній регістр
upper_text = text.upper()

# Підрахунок кількості входжень слова "про" у тексті
word_count = text.count("про")

# Перевірка, чи починається рядок з "Колишня"
starts_with_kolyshnya = text.startswith("Колишня")

print("Текст у верхньому регістрі:")
print(upper_text)
print("\nКількість входжень слова 'про' у тексті:", word_count)
